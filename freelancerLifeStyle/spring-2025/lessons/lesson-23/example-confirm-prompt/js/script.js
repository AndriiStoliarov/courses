"use strict";

// =============================================================
// –ø—Ä–∏–∫–ª–∞–¥ –≤–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω–Ω—è confirm, prompt, —Å—Ç–≤–æ—Ä–µ–Ω–Ω—è —Ç–∞ –¥–æ–¥–∞–≤–∞–Ω–Ω—è –µ–ª–µ–º–µ–Ω—Ç—É –Ω–∞ —Å—Ç–æ—Ä—ñ–Ω–∫—É –¥–ª—è –ø—Ä–æ–≤–µ–¥–µ–Ω–Ω—è —è–∫–æ–≥–æ—Å—å –æ–ø–∏—Ç—É–≤–∞–Ω–Ω—è


// –í–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—î–º–æ confirm –¥–ª—è –æ—Ç—Ä–∏–º–∞–Ω–Ω—è –≤—ñ–¥–ø–æ–≤—ñ–¥—ñ –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á–∞ –Ω–∞ –ø–∏—Ç–∞–Ω–Ω—è "–í–∞–º —Å–ø–æ–¥–æ–±–∞–≤—Å—è —É—Ä–æ–∫"
let confirmAnswer = confirm("–í–∞–º —Å–ø–æ–¥–æ–±–∞–ª–∏—Å—å –Ω–æ—Ç–∞—Ç–∫–∏ –¥–æ —É—Ä–æ–∫—É?");

// –æ–≥–æ–ª–æ—à–µ–Ω–Ω—è –∑–º—ñ–Ω–Ω–æ—ó userResponse –¥–ª—è –∑–±–µ—Ä–µ–∂–µ–Ω–Ω—è –≤—ñ–¥–ø–æ–≤—ñ–¥—ñ
let userResponse
// –Ø–∫—â–æ –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á –≤—ñ–¥–ø–æ–≤—ñ–≤ "—Ç–∞–∫" –Ω–∞ –ø–∏—Ç–∞–Ω–Ω—è "–í–∞–º —Å–ø–æ–¥–æ–±–∞–≤—Å—è —É—Ä–æ–∫?"
if (confirmAnswer === true) {
  // –ü–æ–∫–∞–∑—É—î–º–æ —Å–ø–æ–≤—ñ—â–µ–Ω–Ω—è, —â–æ –º–∏ —Ä–∞–¥—ñ, —â–æ —É—Ä–æ–∫ —Å–ø–æ–¥–æ–±–∞–≤—Å—è –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á—É
  alert("–Ø —Ä–∞–¥–∏–π, —â–æ –≤–∞–º —Å–ø–æ–¥–æ–±–∞–ª–∏—Å—å –Ω–æ—Ç–∞—Ç–∫–∏ –¥–æ —É—Ä–æ–∫—É ü•∞")
} else {
  // –Ø–∫—â–æ –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á –≤—ñ–¥–ø–æ–≤—ñ–≤ "–Ω—ñ" (–∫–Ω–æ–ø–∫–∞ —Å–∫–∞—Å—É–≤–∞—Ç–∏), –º–∏ –ø—Ä–æ—Å–∏–º–æ –π–æ–≥–æ –≤–∫–∞–∑–∞—Ç–∏, —â–æ —Å–∞–º–µ –π–æ–º—É –Ω–µ —Å–ø–æ–¥–æ–±–∞–ª–æ—Å—å
  userResponse = prompt("–í–∫–∞–∂—ñ—Ç—å, —â–æ —Å–∞–º–µ –≤–∞–º –Ω–µ —Å–ø–æ–¥–æ–±–∞–ª–æ—Å—å —Ç–∞ —â–æ –± –≤–∏ –¥–æ–¥–∞–ª–∏ ü§î")

  // –ü–µ—Ä–µ–≤—ñ—Ä—è—î–º–æ —á–∏ –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á –≤–≤—ñ–≤ –≤—ñ–¥–≥—É–∫, —è–∫—â–æ –≤—ñ–Ω –π–æ–≥–æ –≤–≤—ñ–≤ —Ç–æ –≤—ñ–¥–æ–±—Ä–∞–∂–∞—î–º–æ –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è
  if (userResponse !== null && userResponse.trim() !== "") {
    // –ü–æ–∫–∞–∑—É—î–º–æ —Å–ø–æ–≤—ñ—â–µ–Ω–Ω—è, —â–æ –º–∏ –¥—è–∫—É—î–º–æ –∑–∞ –≤—ñ–¥–≥—É–∫ –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á–∞
    alert("–î—è–∫—É—î–º–æ –∑–∞ –≤–∞—à –≤—ñ–¥–≥—É–∫, –º–∏ –ø—Ä–∞—Ü—é—î–º–æ –Ω–∞–¥ –ø–æ–∫—Ä–∞—â–µ–Ω–Ω—è–º ü§©")

    // –æ–≥–æ–ª–æ—à—É—î–º–æ –∑–º—ñ–Ω–Ω—É responceText –≤ —è–∫—ñ–π —Å—Ç–≤–æ—Ä—é—î–º–æ –µ–ª–µ–º–µ–Ω—Ç 
    let responceText = document.createElement('div');

    // –î–æ–¥–∞—î–º–æ –∫–ª–∞—Å 'page__responce' –¥–æ —Å—Ç–≤–æ—Ä–µ–Ω–æ–≥–æ –µ–ª–µ–º–µ–Ω—Ç–∞
    responceText.classList.add('page__responce');

    // –¥–æ–¥–∞—î–º–æ –≤—Å–µ—Ä–µ–¥–∏–Ω—É —Å—Ç–≤–æ—Ä–µ–Ω–æ–≥–æ –µ–ª–µ–º–µ–Ω—Ç—É —â–µ –æ–¥–∏–Ω –µ–ª–µ–º–µ–Ω—Ç –ø–∞—Ä–∞–≥—Ä–∞—Ñ –≤ —è–∫–∏–π –±—É–¥–µ–º–æ –≤–∏–≤–æ–¥–∏—Ç–∏ –≤—ñ–¥–ø–æ–≤—ñ–¥—å –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á–∞
    responceText.innerHTML = `<p>–í–∞—à–∞ –≤—ñ–¥–ø–æ–≤—ñ–¥—å: <span>${userResponse}</span></p>`;

    // –∑–Ω–∞—Ö–æ–¥–∏–º–æ –µ–ª–µ–º–µ–Ω—Ç –¥–æ —è–∫–æ–≥–æ –±—É–¥–µ–º–æ –¥–æ–¥–∞–≤–∞—Ç–∏ –Ω–æ–≤–∏–π –µ–ª–µ–º–µ–Ω—Ç
    const pageContainer = document.querySelector('.page__container');

    // –¥–æ–¥–∞—î–º–æ —Å—Ç–≤–æ—Ä–µ–Ω–π –µ–ª–µ–º–µ–Ω—Ç –≤ page__container
    pageContainer.append(responceText);
  }
}
